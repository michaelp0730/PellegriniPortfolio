        <h2>All About PageRank</h2>
        <h6>By Michael Pellegrini</h6>
        <h5>Learn the fundamentals of PageRank; Google's base algorithym for ranking websites and displaying them in search results.</h5><br />
        <img src="images/printer.gif" alt="Print" width="20" height="20" style="position:relative; top:5px;" />&nbsp;&nbsp;<a href="javascript:window.print()">Print</a>
        <p>Google's PageRank (named after co&ndash;founder Larry Page) was developed by Larry Page and Sergey Brin at Standord University in the late 1990's. PageRank is a link analysis algorithm that assigns a numerical value between 0 and 10 to a given website. There are very few websites with a PageRank of 10 and anything above 6 is considered very good.</p>
        <p>The basic idea behind PageRank is to measure the importance of different websites for the purpose of ranking them in search results. The higher a website's PageRank, the more likely that the site will be ranked highly in a Google search. The PageRank algorithm utilizes over 500 million variables and 2 billion terms, and forms the basis of the Google search engine.</p>
        <p>It seems that the single most important variable in determining a site's PageRank is the number of websites that link to it. However, simply having a large number a websites linking to your website does not mean that you will automatically have a good PageRank. The PageRank of the sites linking to you is a very important factor. In other words, ten links to your site from high ranking sites like <a href="http://www.yahoo.com" target="_new">Yahoo.com</a> or <a href="http://www.msn.com" target="_new">MSN.com</a> will easily outweigh fifty links to your site from unknown/low ranking sites.</p>
        <p>Another extremely important variable in determining PageRank is the site's content. I've said it before and I'll say it again: <strong>Content is King!</strong> Having good, relevant content in your website is vitally important if you're looking to achieve a good PageRank, and ultimately a high search engine listing.</p>
        <p>One way to increase your site's PageRank is to add more pages. A site with five pages (such as this one) will have a maximum page rank of five. By adding more pages, (filled with good content of course) you will increase the possibility of raising your PageRank. It's also important to make sure that your site has no dead&ndash;end pages. When the search engines index your site, they like to follow links from one page to another. That's why I finish each article with a link to my next article. If there are no links to new pages, you are basically telling the crawlers that they have reached the end of the site and it's time to go home.</p>
        <p>If you're willing to pay for it, a great way to increase your PageRank is to purchase a listing in a high quality directory, such as the <a href="http://dir.yahoo.com/" target="_new">Yahoo Directory,</a> or by purchasing text&ndash;based advertising from a company like Google, that links to your site. Incoming links from sites like these will drastically improve your PageRank. There are also plenty of free directories out there that you can submit your site to. One of my personal favorites is the Open Directory Project, located at <a href="http://www.dmoz.org/" target="_new">www.dmoz.org</a>. Many of them won't carry as high of a PageRank as the paid ones, which means they won't help you as much, but they're still better than nothing.</p>
        <p>If you would like to check the PageRank of any website, go to <a href="http://www.prchecker.info/check_page_rank.php" target="_new">www.prchecker.info/check_page_rank.php</a> and type in the url. If you would like to learn more about the mathematical formulas that construct PageRank, here are a couple good sites to visit:</p>
