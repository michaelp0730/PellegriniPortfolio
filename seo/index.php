        <h2>Introduction to Search Engine Optimization</h2>
        <h6>By Michael Pellegrini</h6>
        <h5>The fundamentals of getting your web page to the top of the search engine rankings.</h5><br />
        <img src="images/printer.gif" alt="Print" width="20" height="20" style="position:relative; top:5px;" />&nbsp;&nbsp;<a href="javascript:window.print()">Print</a>
        <p>Welcome to the first of my five-article series on search engine optimization. This article will serve as an introduction to SEO and will provide you with a general understanding of SEO concepts and terminology.</p>
        <p>First of all, what is search engine optimization? Search engine optimization, or SEO for short, refers to a process of optimizing one's website for high ranking/placement in the search engines when a user searches for specific keywords or phrases related to your site. Example: lets say you own a dog grooming business in San Diego, CA and are hoping to increase business by building a website. Your goal is for people to find you through Google, Yahoo, Bing, AOL, etc. Most likely, the way that a user will stumble upon your site is by searching one of the aforementioned sites with a phrase like "dog grooming San Diego." These are some of your main keywords. Keywords are a very important concept in SEO. Before you start developing your site, take some time to think about what keywords and key phrases a user would type into a search engine to find your website. Once you have a list of keywords, start writing content for your site with those keywords and phrases in mind. You don't want to over use these keywords in an unnatural way, but you want to be sure to include them throughout your content. The main point here is to write content that sounds and flows nicely, and sticks to the point that your site is going for. In my final article, <a href="seo_putting_it_all_together.php">SEO &ndash; Putting It All Together</a>, I will go over all the necessary places (besides the actual content of your site) to include your keywords and key phrases.</p>
        <p>There is a long list of items that should be created/adjusted on your website in order to achieve the best possible search engine ranking, but none of them are more important than the content of your site. If you only learn or remember one thing from any of my SEO articles, this should be it: <strong>Content Is King!</strong> Your website must contain relevant content. If you think about it, what do search engines exist for? Search engines were created to assist users in finding websites that are relevant to what they're searching for. The main way that the search engines determine how relevant your site is to a given topic is by reading the content of your site.</p>
        <p>At this point you may be wondering, how do search engines "read" my site? The process is actually known as "indexing" your site and each search engine does it individually. What they do is send out a little piece of software known as a crawler (or robot, bot, spider, etc.) to your website (and every other website on the internet) to read/index the content of the site and report back to the search engine with all of your information. This information is saved in their database and retrieved when a user searches for something related to your site. The search engines use complex algorithms to determine which websites are most important/relevant to a given search, and then displays them in the order that they feel is best. This is known as "page ranking" and every search engine does it a little differently. It's important to note that the algorithms used by the search engines are constantly changing, but that they all work primarily the same way. As long as you follow the rules and have relevant content on your site, you will have nothing to worry about. <a href="all_about_pagerank.php">To learn more about page ranking, visit the fourth article in my series by clicking here.</a></p>
        <p>As you already know, <strong>the content of your website is the single most important factor in developing a successful SEO campaign,</strong> but it's not the only factor. Below is a list of other factors (in no particular order) to consider when optimizing your website for the best possible search engine rankings.</p>
        <ul>
        	<li>Incoming links (links from other websites pointing to your website)</li>
            <li>Your domain name</li>
            <li>The cleanliness of your code</li>
            <li>Use of keywords in Headline tags</li>
            <li>Use of keywords in Meta tags</li>
            <li>Use of keywords in Title tags</li>
            <li>Use of keywords in Alt tags for your images</li>
        </ul>
        <p>Each of these topics will be adressed in later articles, but please note that while using keywords in the appropriate areas of your website is advised, over-using keywords or trying to sneak keywords into your site in unnatural ways is definitely not advised. You may have heard of a variety of tricks or hacks used to try to fool the search engines into giving your site a higher PageRank than it deserves. This is the wrong way to go about SEO. We must remember that the folks who design the search engine algorithms are pretty intelligent people and they are constantly coming up with new ways to catch impostures. It is not uncommon for websites to be removed from search engines altogether because of such hacks. There are definitely right and wrong ways to go about SEO and it is very important for you to understand the differences between the two. To learn more about this, please read my second article titled <a href="seo_what_not_to_do.php">"SEO &ndash; What Not To Do."</a></p>
        <p>Like most web-related fields, SEO is constantly changing and evolving. It has been my experience that as long as you play by the rules and develop your website in a natural way, your PageRank will come along just fine. It may take patience on your end, but as the old saying goes, "good things come to those who wait." Please be sure to check out the rest of the articles in this series. If you have questions, <a href="#" onClick="window.open('contact.php', 'Contact', 'width=425, height=555, scrollbars=no, status=yes');">please feel free to email me by clicking here.</a></p>
