        <h2>SEO &ndash; Putting It All Together</h2>
        <h6>By Michael Pellegrini</h6>
        <h5>How to create a successful SEO campaign for your website.</h5><br />
        <img src="images/printer.gif" alt="Print" width="20" height="20" style="position:relative; top:5px;" />&nbsp;&nbsp;<a href="javascript:window.print()">Print</a>
        <p>Well, you have successfully made it to my final article on search engine optimization, and I thank you for doing so. Over the last four articles I've gone over the fundamentals of SEO, what not to do when optimizing your site, how to clean up your HTML code, and how to increase your PageRank. Now we're going to put together a checklist of all the steps necessary to get you to the top of the search engine listings.</p>
        <p>Lets start from the beginning, and we'll use the same example from my first article about starting a dog grooming business in San Diego. The first step is to come up with a name for your business. I think I'll go with "San Diego Dog Grooming." Once I have the name of my business chosen, I need to purchase the domain name for my website. After doing a search, I found out that www.SanDiegoDogGrooming.com was available, so I purchased it. It's important to note that my main keywords are in my domain name. Next I need to come up with a list of keywords that users would search for when looking for a dog groomer in San Diego. A great way to do this is to use Google Adword's keyword generation tool located at: <a href="https://adwords.google.com/select/KeywordToolExternal" target="_new">https://adwords.google.com/select/KeywordToolExternal</a>.</p>
        <p>Once you have a list of keywords you need to start writing content for your site. Be sure to use these keywords in the site's content, but don't use them in unnatural ways. Write content that sounds and flows nicely. After you've written and edited your content, it's time to start building your website. Remember to write clean HTML code! Don't style your page with HTML, use CSS for that. Close tags that are supposed to be closed and don't use too many images. Imagine that everyone who visits your site is doing so on an old 56k dial&ndash;up modem, and presume that they are in a hurry. Give each page a title with 60 characters or less and be sure to include your main keywords, then save your page with a keyword in the name of the file. Optimize your images to keep their file sizes small, and name your images with appropriate keywords. When you write your image tags, be sure to have an "alt" value with a keyword or phrase in it. <strong>Don't even think about hiding keywords anywhere in your site!</strong> Any text you write should be visible to the naked eye.</p>
        <p>Structure your page with the appropriate HTML tags. Use the h1 tag for the main headline of your page, h2 tag for a secondary headline, p tag for paragraphs, etc. Make sure to use lists as well. Search engines love lists! They're clear, concise, and easy to read. Include keywords and phrases in those lists. Also, don't create pages with dead&ndash;ends. Make sure you link to other pages in your site, otherwise the crawlers will reach the dead&ndash;end and go home without reading the rest of your content.</p>
        <p>Create meta tags in the head of each page. For your meta description tag, write a concise statement in 150 characters or less about that individual page. Include some main keywords and make it easy to read. As for the meta keywords tag, include your most relevant keywords but don't over do it. I'd say 20-30 keywords is enough.</p>
        <p>Once the site is completed, go through it and read everything. Make sure it looks and sounds professional. When you're 100% happy with the site, you can start submitting it to search engines. Here is a list of links to submit your website to the major search engines:</p>
        <ul>
        	<li><a href="http://www.google.com/addurl/?continue=/addurl" target="_new">Google Add URL</a></li>
        	<li><a href="http://siteexplorer.search.yahoo.com/submit" target="_new">Yahoo Site Submit</a></li>
            <li><a href="http://www.bing.com/webmaster/SubmitSitePage.aspx" target="_new">Bing Submit Site</a></li>
        </ul>
        <p>There are also plenty of directories that you can submit your site to. Some cost money, some don't. To find a list of them, simply conduct a Google search for "web directories" or "site submit" and you'll be presented with plenty of options.</p>
        <p>Once again, here is a list of the places that you want to include your keywords:</p>
        <ul>
        	<li>In your domain name</li>
            <li>In the file name of each page</li>
            <li>In the title of each page</li>
            <li>In the headline of each page</li>
            <li>In the meta tags of each page</li>
            <li>In the name of each image</li>
            <li>In the alt tag for each image</li>
            <li>In the content of your site</li>
        </ul>
