        <h2>SEO &ndash; What Not To Do</h2>
        <h6>By Michael Pellegrini</h6>
        <h5>Knowing what not to do in SEO is just as important as knowing what to do.</h5><br />
        <img src="images/printer.gif" alt="Print" width="20" height="20" style="position:relative; top:5px;" />&nbsp;&nbsp;<a href="javascript:window.print()">Print</a>
        <p>When I first started learning about search engine optimization back in 2004, I remember hearing about all these little tricks and hacks that developers were using to get a better listing on the search engines. They would come up with different ways to hide keywords in their websites so that humans couldn't see them, but the crawlers from the search engines could. They would overload their headline tags and alt tags with keywords, and even write horrible articles solely for the purpose of mentioning their keywords as many times as possible, even if it made their article sound as if it were written by a 3rd grader. The worst part about all this is that, at that time, a lot of this stuff actually worked and they were being rewarded for their mischievous development work by landing on the first page of Google for selected searches. Sure we all knew it wasn't really the right way to go about developing a successful SEO campaign, but it was working so lots of people stuck with it.</p>
        <p>Of course, as time went on, the search algorithms got more and more sophisticated and it wasn't long before you started hearing about websites being permanently kicked off the search listings because of their dodgy development practices. And you know what? I'm glad! The reason for this is simple; I'm not only a web developer, but also a user, just like you, and I use search engines just as much as the rest of us. So when I search for something, I want to be presented with the best possible websites for my topic of choice, not just the sites with the most keywords.</p>
        <p>The simple fact is that if you want to show up on the first page of Google (or any other search engine), you need to have a website that is filled with good relevant content, and your content needs to be well written. Forget trying to overload your page with keywords and start focusing on developing a site that will truly be helpful to the users that visit it. Since this article is part of a five&ndash;part series, I'm not going to focus here on what to do for the best possible PageRank, but instead, as the name implies, I'm going to give you a list of things not to do. I'll save all the stuff you should do for a later article, mostly because I truly believe that it is just as important, if not more important, for you to know what not to do with SEO, than it is to know what to do. Remember that the search engines don't like SEO. What they like are well structured websites with relevant content, so as long as you go into the development of your site with the simple intention of building the best possible site, the search engines will notice that and reward you accordingly. However, if they see that you're trying to trick them with dirty development tactics, you will be punished.</p>
        <p>That being said, here is my list of what not to do when developing your website for optimum search engine placement:</p>
        <ul>
        	<li>Don't hide keywords anywhere in your site.</li>
            <li>Don't overload your meta tags, alt tags, or headline tags with keywords. Some is fine, but too many is not good.</li>
            <li>Don't use too many images in your website. They cause the page to load slower and force the crawlers to go through more lines of code to find the relevant content.</li>
            <li>Don't write messy code. Make sure to close any tags that you open.</li>
            <li>Write the least amount of html as possible. Use html for markup, not styling. Style your pages with CSS.</li>
            <li>Use the appropriate html tags. Save your headline tags for headlines and the p tags for paragraphs.</li>
            <li>Don't build a website entirely in Flash. Most search engines can't index all of the content on flash pages, so save your animations for individual parts of a page. A little goes a long way when it comes to animations.</li>
            <li>Most importantly: don't worry about SEO. Focus on creating the best possible website that will help users find what they're looking for and the search engine listings will come naturally.</li>
        </ul>
